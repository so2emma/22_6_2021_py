# assigning color of alien
alien_color = "green"
# correct condition
if alien_color == "green":
    print("You just earned 5 points")

#  incorrect condition
if alien_color != "green":
    print()


# if-else condition
if alien_color == "green":
    print("You just earned 5 points")
else:
    print("You just earned 10 points")

# if-else condition
if alien_color != "green":
    print("You just earned 10 points")
else:
    print("You just earned 5 points")

# if-elif condition
if alien_color == "green":
    print("You just earned 5 points")
elif alien_color == "Yellow":
    print("You just earned 5 points")
elif alien_color == "":
    print("You just earned 5 points")


